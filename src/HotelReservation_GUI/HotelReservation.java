    private void streamData() {
        try {
            File infile = new File("D:/guest.txt");
            Scanner scan = new Scanner(infile);

            //read from file
            while (scan.hasNext()) {
                String name = scan.nextLine();
                String strId = scan.nextLine();
                int id = Integer.parseInt(strId);
                String address = scan.nextLine();
                String room = scan.nextLine();
                String roomType = scan.nextLine();
                String checkIn = scan.nextLine();
                String numNightStr = scan.nextLine();
                int numNight = Integer.parseInt(numNightStr);

                //penambahan array object
                Customer c = new Customer(name, id, address, room, roomType, checkIn, numNight); //Constructor
                guest.add(c);
                String empty = scan.nextLine();
            }
            scan.close();

        } catch (FileNotFoundException | NumberFormatException ex) {
            ex.getStackTrace();
        }
    }

