    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            //get title
            String title = "";
            if (mrRB.isSelected()) {
                title = "Mr. ";
            } else if (MsRB.isSelected()) {
                title = "Ms. ";
            } else {
                throw new IllegalArgumentException();
            }

            //get details
            String name = nameTF.getText();
            int ic = Integer.parseInt(icTF.getText()); //string user will be catch by NumbeFormatex
            String address = addrTA.getText();

            //get room type
            String type, room = null;
            Object typeObj = typeCB.getSelectedItem();
            double price = 0;
            if (typeObj.equals("Single Room")) {
                type = "single";
                price = 100;
            } else if (typeObj.equals("Double Room")) {
                type = "double";
                price = 150;
            } else {
                price = 250;
                type = "Executive Suite";
            }

            //get room type
            Object roomObj = roomCB.getSelectedItem();
            if (roomObj.equals("Standard Collection")) {
                room = "Standard ";
            } else if (roomObj.equals("Premier Collection")) {
                room = "Premier ";
                price = price * 137 / 100;
            }

            //Get Calender
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(jDateChooser1.getDate());
            if (date == null) {
                throw new NullPointerException();
            }

            //calculate the price
            double disc = 0;
            int numNights = Integer.parseInt(numNightsTF.getText());

            if (numNights > 91 || numNights == 0) {
                throw new NumberFormatException();
            }
            price = numNights * price;
            if (numNights > 3) {
                disc = 0.1 * price;
                price = price - disc;
                JOptionPane.showMessageDialog(this, "You get 10% discount from us", "Congratulations", JOptionPane.INFORMATION_MESSAGE);
            }

            //calculate total cost
            double gst;
            double totalCost;
            gst = 0.06 * price;
            totalCost = gst + price;

            DecimalFormat df = new DecimalFormat("#.00"); //2 decimal numbers

            //printing the result
            fnRslt.setText(title + name);
            icRslt.setText("" + ic);
            roomRslt.setText(room + " (" + type + ")");
            priceRslt.setText("" + price);
            gstRslt.setText("" + df.format(gst)); //2 decimal number
            totCostRslt.setText("" + totalCost);
            nightsRslt.setText("" + numNights);
            discTF.setText("" + disc);
            DateCheckIn.setText("" + date);

        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Specify check-in date", "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Have a look your IC number / number of nights", "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "Please specify your title", "Input Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

